    DROP PRIMARY KEY,
    ADD PRIMARY KEY (id, completed_date),
    PARTITION BY RANGE (YEAR(completed_date) * 100 + MONTH(completed_date)) (
        PARTITION p202601 VALUES LESS THAN (202602),
